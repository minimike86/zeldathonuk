def _item_image_url(slug: str, filename: str) -> str:
    """Build a site-relative URL into the frontend assets folder.

    `filename` may contain spaces, apostrophes, parens, ampersands etc.
    Encode it so browsers don't have to guess.
    """
    if not filename:
        return ''
    return f'/assets/img/game-items/{slug}/{quote(filename)}'


