"""`python manage.py populate_zelda_data` — seeds the database with canonical
Zelda titles and a representative item checklist per game.

Idempotent: re-running upserts by title/name. Safe to call repeatedly.

Item icons live under `frontend/public/assets/img/game-items/<slug>/` and
are referenced as site-relative URLs so the OBS browser sources and the
React control panel resolve them straight from the static host.
"""
