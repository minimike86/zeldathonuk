class Command(BaseCommand):
    help = 'Seed canonical Zelda games + item checklists. Idempotent.'

    def handle(self, *args, **options):
        created_games = 0
        created_items = 0
        updated_items = 0
        for spec in GAMES:
            spec = dict(spec)  # local copy so re-running the command doesn't mutate the module
            items = spec.pop('items', [])
            slug = spec.pop('slug', '')
            game, was_new = models.Game.objects.update_or_create(
                title=spec['title'],
                defaults=spec,
            )
            if was_new:
                created_games += 1
            for order, (name, category, image_file) in enumerate(items):
                image_url = _item_image_url(slug, image_file)
                obj, new = models.GameItem.objects.update_or_create(
                    game=game,
                    name=name,
                    defaults={
                        'category': category,
                        'order': order,
                        'image_url': image_url,
                    },
                )
                if new:
                    created_items += 1
                else:
                    updated_items += 1

        self.stdout.write(
            self.style.SUCCESS(
                f'Done. Games processed: {len(GAMES)} (+{created_games} new). '
                f'Items added: {created_items}, updated: {updated_items}.'
            )
        )
